package eo;

public class OrRule {
    int orid;
    int arid;
    int eid;
    int sid;
    int type;
    int order;
    String signal_value;

    public String getSignal_value() {
        return signal_value;
    }

    public void setSignal_value(String signal_value) {
        this.signal_value = signal_value;
    }


    public int getOrid() {
        return orid;
    }

    public void setOrid(int orid) {
        this.orid = orid;
    }

    public int getArid() {
        return arid;
    }

    public void setArid(int arid) {
        this.arid = arid;
    }

    public int getEid() {
        return eid;
    }

    public void setEid(int eid) {
        this.eid = eid;
    }

    public int getSid() {
        return sid;
    }

    public void setSid(int sid) {
        this.sid = sid;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public int getOrder() {
        return order;
    }

    public void setOrder(int order) {
        this.order = order;
    }


}
